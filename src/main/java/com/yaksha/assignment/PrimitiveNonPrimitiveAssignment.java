package com.yaksha.assignment;

public class PrimitiveNonPrimitiveAssignment {

	public static void main(String[] args) {

		// Declare primitive types
		int num1 = 10; // Primitive type
		char letter = 'A'; // Primitive type

		// Declare non-primitive types
		String name = "John"; // Non-primitive type

		// Print the values of variables
		System.out.println("Primitive int: " + num1);
		System.out.println("Primitive char: " + letter);
		System.out.println("Non-primitive String: " + name);
	}
}
